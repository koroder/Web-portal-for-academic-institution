<h6><span class="style2">Frequently asked Questions</span></h6>
<p class="style3"><strong>1. What is the admission process?</strong></p>
<p class="style3">Ans: To get admission into Imperial College, a student is required to appear in a national level exam conducted by the Joint Entrance Committe. The seat is then alloted on rank basis.</p>
<p class="style3"><strong>2. When is the admission test conducted?</strong></p>
<p class="style3">Ans: The admission test is conducted in the first week of April. The results arrive by the end of april.</p>
<p class="style3"><strong>3. What is the syllabus of entrance test?</strong></p>
<p class="style3">Ans: The syllabus is equivalent to your class 12th syllabus. You can get more details with the prospectus.</p>
<p class="style3"><strong>4. Where do I get the application form?</strong></p>
<p class="style3">Ans: The application form will be available at all SBI banks. You can also apply online.</p>
<p class="style3"><strong>5. What is the fees structure of college?</strong></p>
<p class="style3">Ans: The annual fees adds to around 50K INR including tution and hostel/messing charges.</p>
<p class="style3"><strong>6. Where do I get my results?</strong></p>
<p class="style3">Ans: You can view your results online on our website. A confirmation letter will also be sent to the qualified students.</p>
<p class="style3"><strong>7. What about counselling procedure?</strong></p>
<p class="style3">Ans: The counselling will be done online. You can fill in your preferences online, which will then be allotted according to your rank.</p>
<p class="style3"><strong>8. What is the elligibility criteria for appearing in exams?</strong></p>
<p class="style3">Ans: The elligibility criteria stands with securing more than 60% marks in the 10+2 exam.</p>
<p class="style3"><strong>9. What documents need i produce at the time of admission?</strong></p>
<p class="style3">Ans: Marksheet of 10, 10+2 and category certificate (if applicable).</p>
<p class="style3"><strong>10. Is there a limit in number of attempts for qualifying exam?</strong></p>
<p class="style3">Ans: You can appeat for atmost 3 years after passing 10+2 (including the year of passing).</p>
